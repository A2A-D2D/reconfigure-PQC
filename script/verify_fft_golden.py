#!/usr/bin/env python3
"""
Comprehensive Falcon FFT/IFFT Golden Verification Suite
=========================================================

Pre-simulation verification: reads golden vectors generated by
falcon_fft_golden.py and simulates the RTL butterfly operator
(reconfig_fe_f64) to verify lane-by-lane correctness BEFORE
running Verilog simulation.

Key verification: for IFFT, the golden model outputs conj(GM) as the
twiddle. The RTL GS butterfly does y1 = w * (a-b) with NO internal conj.
So feeding w = conj(GM) into the RTL yields the correct Falcon iFFT result.

Test matrix:
  - logN: 9 (Falcon-512), 10 (Falcon-1024)
  - mode: FFT (CT), IFFT (GS)
  - input types: impulse, ramp, random, falcon_poly, all_zeros, single_spike
  - lane patterns: all-5, partial (3-of-5, 1-of-5)

Usage:
  python verify_fft_golden.py                    # quick: N=512, both modes, random
  python verify_fft_golden.py --full             # full matrix
  python verify_fft_golden.py --logn 9 --mode fft
"""

